    #permita ingresar dos números,
    #y con ellos realice las cuatro operaciones básicas(+-*/)
    #luego muestre los resultados
np= float (input ("Ingrese un número: ")) 
ns= float (input("Ingrese otro número: "))

print ("El resultado de la suma de", np, "+", ns, "es: ", np+ns)
print ("El resultado de la resta de", np, "-",ns, "es: " , np-ns)
print ("El resultado de la multiplicación entre", np, "y", ns, "es: ", np*ns)
print ("El resultado de la división entre", np, "y" , ns, "es: ", np / ns)